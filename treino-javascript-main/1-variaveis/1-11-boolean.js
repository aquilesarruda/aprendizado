/**
 * Escreva um script que utilize variáveis number para fazer
 * comparações e atribua os resultados a variáveis boolean, depois imprima os resultados.
 */

const a = 20
const b = 10

const isGreater = a > b // Maior que
const isEqual = a === b // Igual

console.log(`A is greater than B: ${isGreater}`) // A is greater than B: true
console.log(`A is equal to B: ${isEqual}`) // A is equal to B: false
