/**
 * Escreva um script para realizar operações matemáticas básicas (adição,
 * subtração, multiplicação e divisão) com variáveis number e imprimir os resultados.
 */

const num1 = 10
const num2 = 5

const sum = num1 + num2
const subtraction = num1 - num2
const multiplication = num1 * num2
const division = num1 / num2

console.log(`Addition: ${sum}`) // Addition: 15
console.log(`Subtraction: ${subtraction}`) // Subtraction: 5
console.log(`Multiplication: ${multiplication}`) // Multiplication: 50
console.log(`Division: ${division}`) // Division: 2
