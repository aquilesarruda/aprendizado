/**
 * Escreva um script que inverte o valor de uma variável booleana.
 */

let exibeSaldo = true

console.log(`O valor de exibeSaldo no começo é ${exibeSaldo}`)

exibeSaldo = !exibeSaldo // exibeSaldo recebe o contrário de exibeSaldo. Se for true, recebe false. Se for false, recebe true.

console.log(`O valor de exibeSaldo depois é ${exibeSaldo}`)
